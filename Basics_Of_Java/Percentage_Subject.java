public class Percentage_Subject {
    public static void main(String[] args) {
         //Code for calculating percentage
         double x1 = 100; //Maths Marks
         double x2 = 92;  //Chem Marks
         double x3 = 90;  //English Marks
         double x4 = 86;  //Economics Marks
         double x5 = 77;  //Physics Marks
         double p = (x1 + x2 + x3 + x4 + x5)/5;
         System.out.println(p);
    }
}
